        [HttpGet("{shid}")]
        public async Task<IEnumerable<Service>> GetServices(int ShopID)
        {
            return await _mateRepository.GetServices(ShopID);
        }
        [HttpPost]
        public async Task<ActionResult> AddNewService(int id, [FromBody] Service serv)
        {
            var newService = await _mateRepository.AddService(serv);
            return CreatedAtAction(nameof(AddNewService), new { id = newService.ServiceID }, newService);
        }
        [HttpPut]
        public async Task<ActionResult> EditService(int id, [FromBody] Service serv)
        {
            if (id != serv.ServiceID)
            {
                return BadRequest();
            }
            await _mateRepository.EditService(serv);

            return NoContent();
        }
        [HttpDelete]
        public async Task<ActionResult> Delete(int id)
        {
            var deletedService = await _mateRepository.GetService(id);
            if (deletedService == null)
                return NotFound();
            await _mateRepository.DeleteService(deletedService.ServiceID);
            return NoContent();
        }
